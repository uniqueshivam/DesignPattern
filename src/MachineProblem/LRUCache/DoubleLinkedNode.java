package MachineProblem.LRUCache;

public class DoubleLinkedNode {
    Object val;
    String key;
    DoubleLinkedNode next;
    DoubleLinkedNode prev;

    public DoubleLinkedNode(String  key,Object val) {
        this.val = val;
        this.next = next;
        this.prev = prev;
        this.key = key;
    }
}
